    private class ActorImportTransferHandler extends TransferHandler {

        @Override
        public boolean canImport(TransferHandler.TransferSupport support) {
            return support.isDataFlavorSupported(ActorTrans.ACTOR_FLAVOR);
        }

        @Override
        public boolean importData(TransferHandler.TransferSupport support) {
            Transferable transferable = support.getTransferable();
            try {
                Actor add = (Actor) transferable.getTransferData(ActorTrans.ACTOR_FLAVOR);
                if (actors.add(add)) {
                    if (MessageUtils.showConfirmDialog("Add actor", "Are you sure") == JOptionPane.YES_OPTION) {
                        repository.createNewActorMovie(selectedFilm, add);
                        initActorsInMovie(selectedFilm);
                        return true;
                    } else {
                        return false;
                    }

                }
            } catch (UnsupportedFlavorException | IOException ex) {
                Logger.getLogger(User_Form.class.getName()).log(Level.SEVERE, null, ex);
            } catch (Exception ex) {
                Logger.getLogger(User_Form.class.getName()).log(Level.SEVERE, null, ex);
            }
            return false;
        }
    }
    
