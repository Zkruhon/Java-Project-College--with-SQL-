    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (selectedFilm == null) {
            MessageUtils.showInformationMessage("Wrong operation", "Please choose a movie first");
            return;
        }
        if (MessageUtils.showConfirmDialog(
                "Delete movie",
                "Delete movie?") == JOptionPane.YES_OPTION) {
            try {
                Files.deleteIfExists(Paths.get(selectedFilm.getPicturePath()));
                repository.deleteMovie(selectedFilm.getId());
                filmsTableModel.setMovies(repository.selectMovies());
                
                clearForm();
            } catch (Exception ex) {
                Logger.getLogger(User_Form.class.getName()).log(Level.SEVERE, null, ex);
                MessageUtils.showErrorMessage("Error", "Unable to delete movie!");
            }
            
        }
        
    }//GEN-LAST:event_btnDeleteActionPerformed

